function esc(text) {
  return String(text)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function navLink(file, label) {
  return file ? `<a href="${file}">${label}</a>` : `<a href="../index.html#papers">论文选题</a>`;
}

function renderPaper(paper) {
  const sectionLinks = [
    ["question", "研究问题"],
    ["abstract", "摘要"],
    ["contributions", "核心贡献"],
    ["intro", "正文"],
    ["research-design", "研究设计"],
    ["figures", "图表建议"],
    ["evaluation", "评价设计"],
    ["limitations", "局限展望"],
    ["references", "参考文献方向"]
  ];
  const bodySections = paper.sections.map(([title, paragraphs], index) => `
        <article class="paper-section" id="${index === 0 ? "intro" : `section-${index}`}">
          <h2>${esc(title)}</h2>
          ${paragraphs.map((item) => `<p>${esc(item)}</p>`).join("")}
        </article>`).join("");

  return `<!doctype html>
<html lang="zh-CN">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>${esc(paper.shortTitle)}</title>
  <link rel="stylesheet" href="../styles.css">
</head>
<body>
  <header class="site-header">
    <nav class="nav">
      <a class="brand" href="../index.html"><span class="brand-mark">文</span><span>AI 文档工程</span></a>
      <div class="nav-links"><a href="../index.html">首页</a>${navLink(paper.prev, "上一篇")}${navLink(paper.next, paper.next === "five-asset-model.html" ? "回到第一篇" : "下一篇")}</div>
    </nav>
  </header>
  <main>
    <section class="paper-hero">
      <p class="eyebrow">${esc(paper.type)}</p>
      <h1>${esc(paper.title)}</h1>
      <p class="lead">${esc(paper.lead)}</p>
      <div class="paper-meta">${paper.tags.map((tag) => `<span class="tag">${esc(tag)}</span>`).join("")}</div>
    </section>
    <section class="paper-layout">
      <aside class="toc">
        ${sectionLinks.map(([href, label]) => `<a href="#${href}">${label}</a>`).join("")}
      </aside>
      <div class="paper-content">
        <article class="paper-section" id="question">
          <h2>研究问题</h2>
          <p class="quote">${esc(paper.question)}</p>
        </article>
        <article class="paper-section" id="abstract">
          <h2>摘要</h2>
          <p>${esc(paper.abstract)}</p>
          <h3>关键词</h3>
          <div class="tag-row">${paper.keywords.map((kw) => `<span class="tag">${esc(kw)}</span>`).join("")}</div>
        </article>
        <article class="paper-section" id="contributions">
          <h2>核心贡献</h2>
          <ul>${paper.contributions.map((item) => `<li>${esc(item)}</li>`).join("")}</ul>
        </article>${bodySections}
        <article class="paper-section" id="research-design">
          <h2>研究设计</h2>
          ${paper.design.map((item) => `<p>${esc(item)}</p>`).join("")}
        </article>
        <article class="paper-section" id="figures">
          <h2>建议图表</h2>
          <ul>${paper.figures.map((item) => `<li>${esc(item)}</li>`).join("")}</ul>
        </article>
        <article class="paper-section" id="evaluation">
          <h2>评价设计</h2>
          <p>本文可采用案例研究、对比实验、专家评审或企业项目复盘进行验证。核心评价指标包括：</p>
          <ul>${paper.evaluation.map((item) => `<li>${esc(item)}</li>`).join("")}</ul>
        </article>
        <article class="paper-section" id="limitations">
          <h2>局限与未来工作</h2>
          <ul>${paper.limitations.map((item) => `<li>${esc(item)}</li>`).join("")}</ul>
        </article>
        <article class="paper-section" id="references">
          <h2>参考文献方向</h2>
          <p>以下为写成正式投稿论文时建议补充和核验的文献方向：</p>
          <ul>${paper.references.map((item) => `<li>${esc(item)}</li>`).join("")}</ul>
          <div class="actions">
            ${paper.next ? `<a class="button primary" href="${paper.next}">${paper.next === "five-asset-model.html" ? "回到第一篇" : "阅读下一篇"}</a>` : ""}
            <a class="button" href="../index.html#papers">返回选题库</a>
          </div>
        </article>
      </div>
    </section>
  </main>
</body>
</html>
`;
}

